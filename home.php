    <p>Sistem Informasi Akademik SMK Nasional Malang merupakan aplikasi berbasis web. Di aplikasi ini, terdapat pembagian user/multi user yaitu admin,guru dan siswa.
        Selain itu aplikasi ini terdiri dari menu Mata Pelajaran, Jurusan, Standar Kompetensi, Guru, Siswa, Wali Murid, dan Nilai. Diharapkan aplikasi ini dapat memudahkan pekerjaan guru dalam melakukan pendataan serta penilaian siswa.</p>
    <p>Aplikasi ini menggunakan bahasa pemrograman server side (PHP) dan client side (HTML,CSS,dan JAVA SCRIPT), dan menggunakan program XAMPP, Adobe Dreamweaver CC 2015,dan Google Chrome.</p>
